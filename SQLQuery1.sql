 --6. Get all rows of the `works_on` table.
 SELECT *
 FROM works_on

 --7. Get the employee numbers for all employees whose job is `'Analyst'`.

 SELECT emp_no
 FROM works_on
 WHERE job LIKE '%Analyst%'

 --8. Get the employee numbers for employees working on project `202` and having employee numbers lower than `1050`.

 SELECT emp_no
 FROM works_on
 WHERE project_no = '202' AND emp_no < 1050

 --9. Get the employee numbers for employees who didn’t enter their project in `2024`.
 SELECT emp_no
 FROM project AS f
 FULL JOIN works_on AS w ON w.project_no = f.project_no
 WHERE YEAR(enter_date) != 2024

 --10. Get the employee numbers for all employees who have a leading job (i.e., `'Analyst'` or `'Manager'`) in project `201`.
 SELECT emp_no
 FROM works_on
 WHERE (job LIKE '%Analyst%' OR job LIKE '%Manager%' ) AND project_no = 201

--11. Get the enter dates for all employees in project `202` whose jobs have not been determined yet. *(Note: Adjust if no NULL jobs exist)*
SELECT *
FROM works_on
WHERE project_no = 202 AND job is null

--12. Get the employee numbers and last names of all employees whose first names contain two letter `'a'`s (or any repeated letter of your choice).

SELECT emp_no,
	emp_lname
FROM employee
WHERE emp_fname LIKE '%a%' OR emp_fname LIKE '%s%' 

--13. Find the employee numbers of all employees whose department is located in `'Delhi'`.

SELECT *
FROM department
FULL JOIN employee ON employee.dept_no = department.dept_no
WHERE location LIKE '%Delhi%'

--14. Group all departments using their locations *(Show location-wise department count)*.

SELECT 
	dept_name,
	location,
	COUNT(*) AS Dept_count
FROM department AS D
GROUP BY dept_name, location

--15. Get the jobs that are done by more than two employees.
